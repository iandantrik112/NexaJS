/**
 * Catatan Penggunaan:
 * - Gunakan warna yang kontras untuk teks agar mudah dibaca
 * - Untuk background gelap (seperti #24BCA9), gunakan teks putih (#fff)
 * - Untuk background terang (#FFFFFF), gunakan teks hitam (#000000)
 * - StatusBar style "light" = teks putih, "dark" = teks hitam
 */

/**
 * Mendapatkan warna yang dipilih dari data storage
 * @param {Object} Sdk - Data dari storage
 * @returns {Object} - Object dengan selectedColor dan isChecked
 */
export function getSelectedColor(Sdk) {
  const selectedColor = Sdk && Sdk.color ? Sdk.color : null;
  const isChecked = Sdk && Sdk.checked ? true : false;
  return { selectedColor, isChecked };
}

/**
 * Fungsi helper untuk mengubah kecerahan warna
 * @param {string} hex - Kode warna hex
 * @param {number} percent - Persentase perubahan (-100 sampai 100)
 * @returns {string} - Warna hex yang sudah diubah
 */
function adjustBrightness(hex, percent) {
  const num = parseInt(hex.replace("#", ""), 16);
  const amt = Math.round(2.55 * percent);
  const R = (num >> 16) + amt;
  const G = (num >> 8 & 0x00FF) + amt;
  const B = (num & 0x0000FF) + amt;
  return "#" + (0x1000000 + (R < 255 ? R < 1 ? 0 : R : 255) * 0x10000 +
    (G < 255 ? G < 1 ? 0 : G : 255) * 0x100 +
    (B < 255 ? B < 1 ? 0 : B : 255)).toString(16).slice(1);
}

/**
 * Menghasilkan assetColor berdasarkan warna utama yang dipilih
 * @param {string} primaryColor - Warna utama (hex color)
 * @returns {Object} - Object assetColor dengan berbagai variasi warna
 */
export function generateAssetColor(primaryColor) {
  if (!primaryColor) {
    // Default colors jika tidak ada warna yang dipilih
    return {
      backgroundColor: '#24BCA9',
      color: '#009688',
      btnColor: '#e0f2f1',
      iconTextColor: '#000',
      deleteColor: '#f44336',
      deleteBtnColor: '#ffebee',
      buttonColor: '#009688',
      buttonTextColor: '#fff',
      buttonPrevColor: '#e0e0e0',
      buttonPrevTextColor: '#666',
    };
  }

  // Generate warna berdasarkan primary color
  const mainColor = primaryColor;
  const lighterColor = adjustBrightness(mainColor, 30); // Warna lebih terang
  const muchLighterColor = adjustBrightness(mainColor, 60); // Warna jauh lebih terang
  const darkerColor = adjustBrightness(mainColor, -20); // Warna lebih gelap
  // btnColor dibuat 12% lebih terang untuk seimbang dengan backgroundColor dan tetap kontras untuk icon
  const btnColorLighter = adjustBrightness(mainColor, 12);
  
  // Warna untuk teks button (putih atau hitam tergantung kecerahan buttonColor)
  const buttonColor = darkerColor;
  const isButtonLight = getLuminance(buttonColor) > 0.5;
  const buttonTextColor = isButtonLight ? '#000' : '#fff';
  
  // Warna untuk teks icon - lebih seimbang dengan btnColor, bukan hitam/putih murni
  const isBtnColorLight = getLuminance(btnColorLighter) > 0.5;
  let iconTextColor;
  if (isBtnColorLight) {
    // Jika btnColor terang, gunakan warna gelap yang lebih halus (bukan hitam murni)
    iconTextColor = adjustBrightness(btnColorLighter, -60);
  } else {
    // Jika btnColor gelap, gunakan warna terang yang lebih halus (bukan putih murni)
    iconTextColor = adjustBrightness(btnColorLighter, 40);
  }
  
  // Warna untuk teks umum (putih atau hitam tergantung kecerahan mainColor)
  const isLight = getLuminance(mainColor) > 0.5;
  const textColor = isLight ? '#000' : '#fff';
  const lightTextColor = '#666';
  
  return {
    backgroundColor: mainColor,
    color: darkerColor,
    btnColor: btnColorLighter,
    iconTextColor: iconTextColor,
    deleteColor: '#f44336', // Tetap merah untuk delete
    deleteBtnColor: '#ffebee', // Tetap merah muda untuk delete button
    buttonColor: buttonColor,
    buttonTextColor: buttonTextColor,
    buttonPrevColor: '#e0e0e0', // Tetap abu-abu untuk previous button
    buttonPrevTextColor: lightTextColor,
  };
}

/**
 * Menghitung luminance warna untuk menentukan apakah warna terang atau gelap
 * @param {string} hex - Kode warna hex
 * @returns {number} - Luminance value (0-1)
 */
function getLuminance(hex) {
  const rgb = hexToRgb(hex);
  if (!rgb) return 0.5;
  
  const [r, g, b] = [rgb.r, rgb.g, rgb.b].map(val => {
    val = val / 255;
    return val <= 0.03928 ? val / 12.92 : Math.pow((val + 0.055) / 1.055, 2.4);
  });
  
  return 0.2126 * r + 0.7152 * g + 0.0722 * b;
}

/**
 * Konversi hex color ke RGB
 * @param {string} hex - Kode warna hex
 * @returns {Object|null} - Object dengan r, g, b atau null
 */
function hexToRgb(hex) {
  const result = /^#?([a-f\d]{2})([a-f\d]{2})([a-f\d]{2})$/i.exec(hex);
  return result ? {
    r: parseInt(result[1], 16),
    g: parseInt(result[2], 16),
    b: parseInt(result[3], 16)
  } : null;
}

